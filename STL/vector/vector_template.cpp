/*
==================================================
                Vector 模板大全
            蓝桥杯 / 考研机试常用
==================================================
*/

#include <iostream>
#include <vector>
#include <algorithm>
#include <numeric>

using namespace std;

int main()
{
    /*
    ==========================================
                1. 创建 Vector
    ==========================================
    */

    vector<int> v1;                 // 空vector
    vector<int> v2(5);              // 长度为5
    vector<int> v3(5, 100);         // 长度为5，值全为100

    vector<vector<int>> g(3, vector<int>(4, 0)); // 二维vector

    /*
    ==========================================
                2. 添加元素
    ==========================================
    */

    vector<int> v;

    v.push_back(10);
    v.push_back(20);
    v.push_back(30);

    /*
    ==========================================
                3. 访问元素
    ==========================================
    */

    cout << "第一个元素: " << v[0] << endl;

    cout << "最后一个元素: " << v.back() << endl;

    /*
    ==========================================
                4. 获取大小
    ==========================================
    */

    cout << "size = " << v.size() << endl;

    /*
    ==========================================
                5. 遍历 Vector
    ==========================================
    */

    cout << "\n普通for遍历\n";

    for(int i = 0; i < v.size(); i++)
    {
        cout << v[i] << " ";
    }

    cout << endl;

    cout << "\n范围for遍历\n";

    for(auto x : v)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                6. 插入元素
    ==========================================
    */

    v.insert(v.begin() + 1, 99);

    cout << "\ninsert后:\n";

    for(auto x : v)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                7. 删除元素
    ==========================================
    */

    v.erase(v.begin() + 1);

    cout << "\nerase后:\n";

    for(auto x : v)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                8. 删除最后一个元素
    ==========================================
    */

    v.pop_back();

    cout << "\npop_back后:\n";

    for(auto x : v)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                9. 排序
    ==========================================
    */

    vector<int> a = {5, 2, 8, 1, 9};

    sort(a.begin(), a.end());

    cout << "\n升序排序:\n";

    for(auto x : a)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                10. 降序排序
    ==========================================
    */

    sort(a.begin(), a.end(), greater<int>());

    cout << "\n降序排序:\n";

    for(auto x : a)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                11. 反转
    ==========================================
    */

    reverse(a.begin(), a.end());

    cout << "\nreverse后:\n";

    for(auto x : a)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                12. 查找元素
    ==========================================
    */

    auto it = find(a.begin(), a.end(), 8);

    if(it != a.end())
    {
        cout << "\n找到元素8\n";
    }

    /*
    ==========================================
                13. 二分查找
    ==========================================
    */

    sort(a.begin(), a.end());

    if(binary_search(a.begin(), a.end(), 5))
    {
        cout << "存在5\n";
    }

    /*
    ==========================================
                14. 最大值 最小值
    ==========================================
    */

    cout << "\n最大值: "
         << *max_element(a.begin(), a.end())
         << endl;

    cout << "最小值: "
         << *min_element(a.begin(), a.end())
         << endl;

    /*
    ==========================================
                15. 求和
    ==========================================
    */

    int sum = accumulate(a.begin(), a.end(), 0);

    cout << "总和: "
         << sum
         << endl;

    /*
    ==========================================
                16. 去重
    ==========================================
    */

    vector<int> b =
    {
        1,1,2,2,3,3,4,4,5
    };

    sort(b.begin(), b.end());

    b.erase(
        unique(b.begin(), b.end()),
        b.end()
    );

    cout << "\n去重后:\n";

    for(auto x : b)
    {
        cout << x << " ";
    }

    cout << endl;

    /*
    ==========================================
                17. 清空 Vector
    ==========================================
    */

    b.clear();

    cout << "\nclear后 size = "
         << b.size()
         << endl;

    /*
    ==========================================
                18. 判断是否为空
    ==========================================
    */

    if(b.empty())
    {
        cout << "vector为空\n";
    }

    /*
    ==========================================
                19. Vector模拟栈
    ==========================================
    */

    vector<int> st;

    st.push_back(10);
    st.push_back(20);
    st.push_back(30);

    cout << "\n栈顶元素: "
         << st.back()
         << endl;

    st.pop_back();

    cout << "弹出后栈顶: "
         << st.back()
         << endl;

    /*
    ==========================================
                20. 二维Vector
    ==========================================
    */

    vector<vector<int>> matrix =
    {
        {1,2,3},
        {4,5,6},
        {7,8,9}
    };

    cout << "\n二维vector:\n";

    for(auto row : matrix)
    {
        for(auto x : row)
        {
            cout << x << " ";
        }
        cout << endl;
    }

    return 0;
}
