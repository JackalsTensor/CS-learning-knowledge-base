/*
==================================================
            C++ Struct Template
==================================================
*/

#include <iostream>
#include <string>
#include <algorithm>

using namespace std;

/*
==================================================
            1. 结构体定义
==================================================
*/
struct Student
{
    string name;
    int age;
    double score;
};

/*
==================================================
            2. 结构体数组排序函数
==================================================
*/
bool cmp(Student a, Student b)
{
    return a.score > b.score;
}

int main()
{
    /*
    ==================================================
                3. 创建结构体变量
    ==================================================
    */

    Student s1;

    s1.name = "Tom";
    s1.age = 18;
    s1.score = 95.5;

    cout << s1.name << endl;
    cout << s1.age << endl;
    cout << s1.score << endl;


    /*
    ==================================================
                4. 初始化结构体
    ==================================================
    */

    Student s2 =
    {
        "Jack",
        19,
        88.5
    };

    cout << "\n初始化方式\n";

    cout << s2.name << " "
         << s2.age << " "
         << s2.score
         << endl;


    /*
    ==================================================
                5. 结构体数组
    ==================================================
    */

    Student stu[3] =
    {
        {"Alice",18,90},
        {"Bob",20,85},
        {"Mike",19,95}
    };

    cout << "\n结构体数组\n";

    for(int i=0;i<3;i++)
    {
        cout << stu[i].name << " "
             << stu[i].age << " "
             << stu[i].score
             << endl;
    }


    /*
    ==================================================
                6. 结构体排序
    ==================================================
    */

    sort(stu, stu+3, cmp);

    cout << "\n按成绩降序排序\n";

    for(int i=0;i<3;i++)
    {
        cout << stu[i].name << " "
             << stu[i].score
             << endl;
    }


    /*
    ==================================================
                7. 结构体指针
    ==================================================
    */

    Student *p = &s1;

    cout << "\n结构体指针\n";

    cout << p->name << endl;
    cout << p->age << endl;


    /*
    ==================================================
                8. 结构体传参
    ==================================================
    */

    Student temp =
    {
        "Lucy",
        20,
        99
    };

    cout << "\n结构体对象\n";

    cout << temp.name << endl;


    /*
    ==================================================
                9. 结构体数组查找
    ==================================================
    */

    string target = "Alice";

    for(int i=0;i<3;i++)
    {
        if(stu[i].name == target)
        {
            cout << "\n找到学生："
                 << stu[i].name
                 << endl;
        }
    }


    /*
    ==================================================
                10. 嵌套结构体
    ==================================================
    */

    struct Date
    {
        int year;
        int month;
        int day;
    };

    struct Employee
    {
        string name;
        Date birthday;
    };

    Employee e =
    {
        "ZhangSan",
        {2005,5,1}
    };

    cout << "\n嵌套结构体\n";

    cout << e.name << endl;

    cout << e.birthday.year << "-"
         << e.birthday.month << "-"
         << e.birthday.day
         << endl;

    return 0;
}
