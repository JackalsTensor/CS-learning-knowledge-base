#导包
import torch

#1.定义变量,记录:初始的权重w(旧)
w=torch.tensor(10,requires_grad=True,dtype=torch.float)

#2.定义loss变量,表示损失函数
loss=2*w**2+20

#3.打印梯度函数类型
# print(f'初始的loss是：{loss}')
# print(f'梯度函数类型:{type(loss.grad_fn)}')
# print(loss.sum())

#迭代100次，求最优解
for i in range(1,101):
    #3.1正向计算(前向传播)
    loss=2*w**2+20
    #3.2梯度清零
    if w.grad is not None:
        w.grad.zero_()
    #3.3反向传播
    loss.sum().backward()
    #3.4带入权重更新公式:w新=w旧-学习率*梯度
    w.data=w.data-0.01*w.grad.data
    print(f'第{i}次迭代，w={w},loss={loss}')
print(f'更新后的loss是：{loss}')

#6.打印最终结果
print(f'更新后的w是：{w}')
print(f'更新后的loss是：{loss}')
print(f'梯度函数类型:{type(loss.grad_fn)}')
print(loss.sum())

