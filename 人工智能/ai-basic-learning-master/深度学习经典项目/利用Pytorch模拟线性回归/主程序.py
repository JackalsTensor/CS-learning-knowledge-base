#导入相关库
import torch
from torch.utils.data import TensorDataset      #构建数据集对象
from torch.utils.data import DataLoader         #数据加载器
from torch import nn                            #nn中有平方根损失函数和假设函数
from torch import optim                         #optim中有优化器函数
from sklearn.datasets import make_regression    #创建模型回归类型数据集
import matplotlib.pyplot as plt

plt.rcParams['font.sans-serif'] = ['SimHei']  #用来正常显示中文标签
plt.rcParams['axes.unicode_minus'] = False #用来正常显示负号

#1.定义函数,创建线性回归样本数据
def create_dataset():
    x,y,coef = make_regression(n_samples=100, #100个样本点
                               n_features=1,  #1个特征
                               noise=10,     #10个噪声
                               coef=True,     #是否返回系数
                               random_state=3 #随机数种子
                             )
    #2.把上述数据,封装成张量对象
    x=torch.tensor(x,dtype=torch.float32)
    y=torch.tensor(y,dtype=torch.float32)
    #3.返回结果
    return x,y,coef

#2.定义函数,表示模型训练
def train(x,y,coef):
    #1.创建数据集对象，把tensor->数据集对象->数据加载器
    dataset=TensorDataset(x,y)
    #2.创建数据加载器对象
    #参一:数据集对象,参二:批次大小,参数三:是否打乱数据(训练集打乱,测试集不打乱)
    dataloader=DataLoader(dataset,batch_size=16,shuffle=True)
    #3.创建初始的线性回归模型
    #参一:输入特征数,参二:输出特征数
    model=nn.Linear(in_features=1,out_features=1)
    #4.创建损失函数对象
    criterion=nn.MSELoss()
    #5.创建优化器对象
    optimizer=optim.SGD(model.parameters(),lr=0.01)
    #6.具体的训练过程
    #6.1 定义变量，分别表示测试轮数，每轮的平均损失，训练总损失值，总样本数
    epochs,loss_list=100,[]
    #6.2 开始训练
    for epoch in range(epochs):
        # 每轮开始重置损失与计数（这是我只加的关键修复）
        total_loss,total_sample=0,0
        #6.3 每轮是分批次训练
        for train_x,train_y in dataloader:
            #6.4 模型预测
            y_pred=model(train_x)
            #6.5 计算(每轮的平均)损失值
            loss=criterion(y_pred,train_y.reshape(-1,1))
            #6.6 计算损失和样本批次数
            total_loss+=loss.item()
            total_sample+=1
            #6.7 梯度清零+反向传播+梯度更新
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()
    #6.8 把每轮的平均损失添加到列表中
        loss_list.append(total_loss/total_sample)
        print(f'第{epoch+1}轮，平均损失是：{total_loss/total_sample}')
    #7.返回结果
    print(f'{epochs}轮的平均损失分别为:{loss_list}')
    print(f'模型参数,权重是：{model.weight},偏置是：{model.bias}')
    #8.绘制损失曲线
    plt.plot(range(epochs),loss_list)
    plt.title('损失曲线变化图')
    plt.grid()
    plt.show()
    #9.绘制预测结果和真实结果关系
    plt.scatter(x,y,c='r',s=10)
    plt.plot(x,model(x).detach().numpy(),c='b',linewidth=3)
    plt.title('预测结果和真实结果关系')
    plt.grid()
    plt.show()

#3.测试
if __name__ == '__main__':
    #3.1创建数据集
    x,y,coef=create_dataset()
    # print(f'系数是：{coef}')
